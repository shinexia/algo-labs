    public static String toString(ListNode head) {
        StringBuilder sb = new StringBuilder();
        sb.append("[");
        ListNode current = head;
        while (current != null) {
            if (current != head) {
                sb.append(",");
            }
            sb.append(current.val);
            current = current.next;
        }
        return sb.append("]").toString();
    }

